; This is a program written to print out an ascii character from three different inputs.
; It prints out on 8x16 window.
; To accomplish this, the program utilizes iterative and conditional constructs
; and goes through all the values in the inputs in order to print out the desired pattern
; R6 is used to hold the value that represents the hexadecimal character that the program is supposed to print
; R3 is the counter for the outer loop which is the row loop
; R2 is the counter for the inner loop which is the column loop
; R5 is initially set to hold the address of the font_data label
; R4 hold the sum of R5 and R6 to serve as a pointer for where the program needs to begin
; R5's functionn then changes to hold the value of the register that the pointer R4 holds, so that the computer can actually take in the data
; R0 is used to hold values that the computer is supposed to print.
