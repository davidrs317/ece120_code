		ADD R6, R6, R6	; Multiply R6 by two, this is part of multiplying it by 16
		ADD R6, R6, R6	; Multiply R6 by two, this is part of multiplying it by 16
		ADD R6, R6, R6	; Multiply R6 by two, this is part of multiplying it by 16
		ADD R6, R6, R6	; Multiply R6 by two, this is part of multiplying it by 16
		LEA R5, FONT_DATA	; This loads the address of font_data into R5
		ADD R4, R5, R6	; Add R5 and R6 and place the result into R4, this lets R4 serve as a pointer

SCAN		LDR R5, R4, #0	; Set R5 equal to the value of the register represented by the data in R4. This will allow the computer to scan+print data
		ADD R3, R3, #0	; Perform a function upon R3 so that the Branch function can be utilized
		BRn STOP	; If R3 is negative, stop the program, else continue

COLUMNLOOP	ADD R2, R2, #0	; Perform a negligible function to R2 so that the Bracnh function can be utilized
		Brz DEC1	; If R2 is zero go to the point in the code where R3(the outer loop) is decremented)
		ADD R5, R5, #0	; Perform a negligible function upon R5 so that branch can be utilized
		Brn PRINTONE	; If R5 is negative; go to the point in the code where the character for a one will be printed

		LDI R0, ZEROCHAR	; Fill R0 with the value held in the value held in x5000
		OUT	; Print R0
		ADD R2, R2, #-1; Decrement R2
		ADD R5, R5, R5; Left shift R5
		BRnzp COLUMNLOOP ; In all cases go back to the code at the COLUMNLOOP LABEL

PRINTONE	LDI R0, ONECHAR	; Fill R0 with the value held in memory at the value held in x5001
