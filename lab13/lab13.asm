; R0 holds values to be printed
; R1 is the column counter
; R2 is the row counter
; R4 serves as a pointer for the memory location at which the characters are stored
; R5 is like a row counter that increments each row so that the program points to the address to start each row
; R6 holds the starting address of the program
; This is a program designed to take in an input of a word or character and printing it to the screen. To do such it utilizes iterative and conditional constructs
; It takes in inputs of the word one character at a time and iterates over the memory addresses that the characters of the word are held in
; It then evaluates whether the current bit that it is over is a one or a zero
; It will thhhen print out one ascii character or another depending
; Once the program has gone over all the characters and printed 16 rows of text it will terminate
; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

		.ORIG x3000
		AND R0, R0, #0	; Clear R0 so it can be updated
		AND R1, R1, #0	; Clear R1 so it can be updated
		AND R2, R2, #0	; Clear R2 so it can be updated
		AND R4, R4, #0	; Clear R4 so it can be updated
		AND R5, R5, #0	; Clear R5 so it can be updated
		AND R6, R6, #0	; Clear R6 so it can be updated

		ADD R2, R2, #15	; set R2 to 15 to serve as a row counter
		ADD R1, R1, #8	; set R1 to 8 to serve as a column counter

LINE		LD R6, AWORD	; set R6 equal to the address of AWORD
		LDR R4, R6, #0	; set R4 to the value held in address AWORD
		Brz NULL	; go here if the first input is null
CHAR		ADD R4, R4, R4	; multiply R6 by two, a part of multiplying it by 16 to find the desired address
		ADD R4, R4, R4	; multiply R6 by two, a part of multiplying it by 16 to find the desired address
		ADD R4, R4, R4	; multiply R6 by two, a part of multiplying it by 16 to find the desired address
		ADD R4, R4, R4	; multiply R6 by two, a part of multiplying it by 16 to find the desired address

		LEA R0, FONT_DATA	; load the address of font_data ito R0
		ADD R4, R4, R0	; add the value of R4 and R0 into R4 so the sum can later be stored as a pointer
		ADD R4, R4, R5	; by adding R5 to R4, this will send the program to the correect row
		LDR R4, R4, #0	; store the value held in R4 into R4 so that R4 can serve as a pointer
		
		ADD R2, R2, #0	; perform a function on R2 so the branch function can analyze it
		Brn STOP	; if the row counter is negative, that means 16 rows have been printed and the program must stop

COLUMN		ADD R1, R1, #0	; perform a function on R1 so the branch function can analyze it
		Brz NEXTCHAR	; if R1 is zero, that means that 8 columns have been printed and the next character or a new line must be printed
		 
		ADD R4, R4, #0	; perform a function on R4 so the branch function can analyze it
		Brn PRINTONE	; if R4 holds a negative value jump to the code that prints the character for a one

		LDI R0, ZEROCHAR ; load the value held in the address held in the address at label ZEROCHAR into RO
		OUT		; print the value in R0
		ADD R1, R1, #-1	; decrement R1, the column counter
		ADD R4, R4, R4	; left shift R4
		Brnzp COLUMN 	; go back to when the value of the column was checked

PRINTONE	LDI R0, ONECHAR	; load the value held in the address held in the address at label ZEROCHAR into R0
		OUT		; print the value in RO
		ADD R1, R1, #-1	; decrement R1, the column counter
		ADD R4, R4, R4	; left shift R4
		Brnzp COLUMN	; go back to when the value of the column counter was checked

NEXTCHAR	ADD R6, R6, #1	; increment the address counter to find characters
		LDR R4, R6, #0	; load the new address for R4 to point to
		Brnz NEWLINE	; if the new value in R4 is not positive that means a new line must begin

		ADD R1, R1, #8	; reset the column counter
		Brnzp CHAR	; send the computer back to the address where the current character is found

NEWLINE		LD R0, LINERETURN	; set R0 equal to the hexadecimal value for a newline string
		OUT		; print the new line
		ADD R2, R2, #-1 ; decrement the row counter
		ADD R1, R1, #8	; reset the column counter
		ADD R5, R5, #1	; add 1 to R5 which leads the program to the loop that its supposed to be on
		Brnzp LINE	; go back to the instruction set which sets R4 to the new desired address

NULL		LD R0, LINERETURN	; load R0 with the new line value
		OUT		; print R0
		ADD R2, R2, #-1	; decrement the row counter
		BRzp NULL	; if R2 is zero or positive go back to NULL, elsewise stop the program	

STOP		HALT		; stop the program 		


